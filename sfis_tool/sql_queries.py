# --- WIP ---
WIP_GET_STATION_AND_NEXT = """
SELECT A.SERIAL_NUMBER, A.MO_NUMBER, A.MODEL_NAME,
       A.STATION_NAME, A.LINE_NAME, A.GROUP_NAME,
       (SELECT R.GROUP_NEXT FROM SFIS1.C_ROUTE_CONTROL_T R
        WHERE R.ROUTE_CODE = A.SPECIAL_ROUTE
          AND R.GROUP_NAME = A.GROUP_NAME
          AND R.STATE_FLAG = A.ERROR_FLAG
          AND ROWNUM = 1) AS NEXT_STATION
FROM SFISM4.R_WIP_TRACKING_T A
WHERE A.SERIAL_NUMBER = :sn
"""

