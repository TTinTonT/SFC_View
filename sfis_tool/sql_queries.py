# --- Repair fail flow ---
REPAIR_FAIL_HISTORY = """
SELECT
    r.SERIAL_NUMBER,
    r.TEST_TIME,
    r.GROUP_NAME,
    r.TEST_CODE                           AS ERROR_CODE_IN_REPAIR,
    e.ERROR_CODE                          AS ERROR_CODE_MASTER,
    e.ERROR_DESC
FROM SFISM4.R_REPAIR_T r
LEFT JOIN SFIS1.C_ERROR_CODE_T e
       ON REPLACE(e.ERROR_CODE, ' ', '') = REPLACE(r.TEST_CODE, ' ', '')
WHERE r.SERIAL_NUMBER = :sn
  AND NVL(r.RECORD_TYPE, 'T') = 'T'
ORDER BY NVL(r.REPAIR_TIME, r.TEST_TIME), r.TEST_TIME
"""

