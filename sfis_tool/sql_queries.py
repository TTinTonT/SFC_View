JUMP_CHECK_JUMP_STATION = """
SELECT * FROM (
    WITH TEMP_ROUTE AS (
        SELECT ROUTE_CODE, GROUP_NAME, GROUP_NEXT, STEP_SEQUENCE
        FROM SFIS1.C_ROUTE_CONTROL_T WHERE STATE_FLAG = '0'
        AND ROUTE_CODE = (SELECT SPECIAL_ROUTE FROM SFISM4.R_WIP_TRACKING_T WHERE SERIAL_NUMBER = :sn)
        ORDER BY STEP_SEQUENCE
    ),
    TEMP_KITTING AS (
        SELECT ROUTE_CODE, GROUP_NAME, STEP_SEQUENCE FROM TEMP_ROUTE
        WHERE GROUP_NAME IN (SELECT GROUP_NAME FROM SFIS1.KITTING_GROUP_V)
    ),
    TEMP_TARGET AS (
        SELECT GROUP_NAME AS TARGET_GROUP, STEP_SEQUENCE AS TARGET_SEQ FROM TEMP_ROUTE WHERE GROUP_NAME = :g
    )
    SELECT * FROM TEMP_KITTING T1 LEFT JOIN TEMP_TARGET T2 ON 1=1 WHERE T2.TARGET_SEQ > T1.STEP_SEQUENCE
)
"""

